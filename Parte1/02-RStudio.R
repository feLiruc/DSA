# operadores básicos, relacionas e lógicos em R

# Operadores básicos

# Soma
4+4

# Subtração
4-3

# Multiplicação
4*4

# Divisão
4/3

# Potência
4 ^ 2
4 ** 2

# Módulo
14 %% 3

# Operadores relacionais
# Atribuindo variáveis
a = 7
b = 5

# Operadores
a > 8
a < 8
a <= 8
a >= 8
a == 8
a != 8

# Operadores lógicos
# AND
(a==8) & (b==6)
(a==7) & (b==5)

# OR
(a==8) | (b==6)
(a==8) | (b==5)

# Not
a > 8
print(!a > 8)